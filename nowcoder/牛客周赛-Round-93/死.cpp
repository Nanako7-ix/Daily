void solve() {
    int n, k;
    cin >> n >> k;
    vector<int> a(n + 1);
    for (int i = 1; i <= n; ++i) {
        cin >> a[i];
    }
    vector<int> id(n + 1), vis(n + 1);
    iota(id.begin(), id.end(), 0);
    sort(id.begin() + 1, id.end(), [&](int i, int j) {
        if (a[i] == a[j]) {
            return i > j;
        } return a[i] > a[j];
    });

    for (int i = 1, cnt = 1; cnt <= k; ++i) {
        if (id[i] == 1) continue;
        vis[id[i]] = 1, cnt++;
        a[1] += a[id[i]];
    }

    for (int i = 1; i <= n; ++i) {
        if (vis[i]) continue;
        cout << a[i] << ' ';
    }
    cout << endl;
}

signed main() {
    cin.tie(0), cout.tie(0);
    ios::sync_with_stdio(0);

    int T = 1;
    cin >> T;

    while (T--) {
        solve();
    }

    return 0;
}