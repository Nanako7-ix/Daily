void solve() {
    int n;
    cin >> n;
    vector<vector<int>> a(n + 1);
    for (int i = 1; i <= n; ++i) {
        a[i].resize(n + 1);
        iota(a[i].begin(), a[i].end(), 0);
    }
    auto modify = [&](int p, int x, int y) {
        cout << p << ' ' << x << ' ' << y << '\n';
        for (int l = x, r = y; l < r; ++l, --r) {
            swap(a[p][l], a[p][r]);
        }
    };
    cout << 2 * n - 2 << "\n";
    modify(1, 2, n);
    for (int i = 2; i <= n; ++i) {
        modify(i, 1, i);
        if (i != n) {
            modify(i, i + 1, n);
        }
    }
}

signed main() {
    cin.tie(0), cout.tie(0);
    ios::sync_with_stdio(0);

    int T = 1;
    cin >> T;

    while (T--) {
        solve();
    }

    return 0;
}