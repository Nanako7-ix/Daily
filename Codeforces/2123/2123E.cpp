void solve() {
    int n;
    cin >> n;
    vector<int> cnt(n + 1);
    for (int i = 1; i <= n; ++i) {
        int x;
        cin >> x;
        cnt[x]++;
    }

    vector<int> dis(n + 2);
    for (int i = 0; i <= n; ++i) {
        dis[cnt[i]]++, dis[n - i + 1]--;
        if (cnt[i] == 0) break;
    }

    for (int i = 0; i <= n; ++i) {
        cout << dis[i] << " \n"[i == n];
        dis[i + 1] += dis[i];
    }
}

signed main() {
    cin.tie(0), cout.tie(0);
    ios::sync_with_stdio(0);

    int T = 1;
    cin >> T;

    while (T--) {
        solve();
    }

    return 0;
}