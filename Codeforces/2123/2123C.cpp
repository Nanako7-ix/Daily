#include <bits/stdc++.h>
#include <ext/pb_ds/assoc_container.hpp>
#include <ext/pb_ds/tree_policy.hpp>
#define debug(x) cerr << "\033[31m" << #x << " = " << x << "\033[0m\n"
using namespace std;
using namespace __gnu_pbds;

using i64 = long long;
using u64 = unsigned long long;
using u32 = unsigned;
using u128 = unsigned __int128;
using i128 = __int128;
using f64 = double;

constexpr int inf = 0x3f3f3f3f;
constexpr i64 INF = 0x3f3f3f3f3f3f3f3f;

void solve() {
    int n;
    cin >> n;
    vector<int> a(n + 1);
    for (int i = 1; i <= n; ++i) {
        cin >> a[i];
    }

    vector<int> pre(n + 1), suf(n + 1);
    pre[1] = a[1], suf[n] = a[n];
    for (int i = 2; i <= n; ++i) {
        pre[i] = min(pre[i - 1], a[i]);
    }
    for (int i = n; i >= 2; --i) {
        suf[i - 1] = max(suf[i], a[i - 1]);
    }

    map<int, bool> ok;
    for (int i = 1; i <= n; ++i) {
        ok[a[i]] |= pre[i] >= a[i] || suf[i] <= a[i];
    }

    for (int i = 1; i <= n; ++i) {
        cout << ok[a[i]];
    }
    cout << "\n";
}

signed main() {
    cin.tie(0), cout.tie(0);
    ios::sync_with_stdio(0);

    int T = 1;
    cin >> T;

    while (T--) {
        solve();
    }

    return 0;
}