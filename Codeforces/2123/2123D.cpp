void solve() {
    int n, k;
    string s;
    cin >> n >> k >> s;
    
    int Max = 0, cnt = 0;

    vector<int> a(n + 1);
    for (int i = 1; i <= n; ++i) {
        a[i] = i > k || i <= n - k;
        if (a[i]) cnt += s[i - 1] == '1';
        a[i] += a[i - 1];
    }
    
    for (int l = 1, r = k; r <= n; ++l, ++r) {
        Max = max(Max, a[r] - a[l - 1]);
    }

    if (cnt <= k || Max < k) {
        cout << "Alice\n";
    } else {
        cout << "Bob\n";
    }
}

signed main() {
    cin.tie(0), cout.tie(0);
    ios::sync_with_stdio(0);

    int T = 1;
    cin >> T;

    while (T--) {
        solve();
    }

    return 0;
}