void solve() {
    int a, b, x, y;
    cin >> a >> b >> x >> y;
    if (a > b) {
        if ((a ^ 1) == b) {
            cout << y << "\n";
        } else {
            cout << "-1\n";
        }
    } else {
        int ans = 0;
        while (a != b) {
            if (a % 2 == 0) {
                ans += min(x, y);
            } else {
                ans += x;
            }
            ++a;
        }
        cout << ans << "\n";
    }
}

signed main() {
    cin.tie(0), cout.tie(0);
    ios::sync_with_stdio(0);

    int T = 1;
    cin >> T;

    while (T--) {
        solve();
    }

    return 0;
}