#include <bits/stdc++.h>
#include <ext/pb_ds/assoc_container.hpp>
#include <ext/pb_ds/tree_policy.hpp>
#define debug(x) cerr << "\033[31m" << #x << " = " << x << "\033[0m\n"
using namespace std;
using namespace __gnu_pbds;

using i64 = long long;
using u64 = unsigned long long;
using u32 = unsigned;
using u128 = unsigned __int128;
using i128 = __int128;
using f64 = double;

constexpr int inf = 0x3f3f3f3f;
constexpr i64 INF = 0x3f3f3f3f3f3f3f3f;

void solve() {
    i64 x, y;
    cin >> x >> y;

    auto f = [&](int n) -> int {
        return n - 1 + (x + n - 1) / n + (y + n - 1) / n;
    };

    int l = 1, r = max(x, y), ans = x + y;
    while (l <= r) {
        int m1 = (l + l + r) / 3;
        int m2 = (l + r + r) / 3;
        if (f(m1) < f(m2)) {
            ans = min(ans, f(m1));
            r = m2 - 1;
        } else {
            ans = min(ans, f(m2));
            l = m1 + 1;
        }
    }
    for (int i = max(l - 500, 1); i <= l + 500; ++i) {
        ans = min(ans, f(i));
    }
    cout << ans << "\n";
}

signed main() {
    cin.tie(0), cout.tie(0);
    ios::sync_with_stdio(0);

    int T = 1;
    cin >> T;

    while (T--) {
        solve();
    }

    return 0;
}