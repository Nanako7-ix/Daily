void solve() {
    string s;
    cin >> s;
    int n = s.size();
    vector<int> a(n + 1);
    a[n] = s[n - 1] - '0';
    for (int i = n - 1; i >= 1; --i) {
        a[i] = a[i + 1];
        while (a[i] % 10 != s[i - 1] - '0') {
            a[i]++;
        }
    }
    cout << a[1] + n << endl;
}

