void solve() {
    int n;
    cin >> n;
    vector<int> a(n + 1);
    for (int i = 1; i <= n; ++i) {
        cin >> a[i];
    }
    sort(a.begin(), a.end());
    int l0 = n, l1 = n, r0 = -1, r1 = -1;
    for (int i = 1; i <= n; ++i) {
        if (a[i] % 2 == 0) l0 = min(l0, i), r0 = max(r0, i);
        if (a[i] % 2 == 1) l1 = min(l1, i), r1 = max(r1, i);
    }
    cout << n - max(r0 - l0 + 1, r1 - l1 + 1) << endl;
}

signed main() {
    cin.tie(0), cout.tie(0);
    ios::sync_with_stdio(0);

    int T = 1;
    cin >> T;

    while (T--) {
        solve();
    }

    return 0;
}